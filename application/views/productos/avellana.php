                        <h2 style="color: white;">Sus propiedades</h2>
                        <p>
                        Su composición está conformada de la siguiente manera considerando 100 gr en crudo:
                        <ul>
                        <li>13gr de proteína

                        <li>65gr de grasa

                        <li>5gr de hidratos de carbono

                        <li>10gr de fibra

                        <li>Calorías : 650

                        <li>Las vitaminas A , E ( 25 mg) y Ac . Fólico .

                        <li>Minerales: calcio ( 140 mg) y magnesio.
                        </ul>
                        <br>
Las avellanas poseen un alto valor nutricional 16% de proteínas y 62% de  grasas mono y poliinsaturadas, además contiene significativos niveles de tiamina, niacina y altos niveles de calcio, fósforo, magnesio y potasio.
<br><br>
Las avellanas, como todos los vegetales, no tienen colesterol. Su proteína vegetal es de muy buena calidad, ya que contiene una elevada proporción de arginina este aminoácido es muy importante para el buen funcionamiento del sistema cardiovascular. La riqueza de su composición nutricional se complementa con fibra vegetal, vitaminas, sales minerales y oligoelementos.
<br><br>
Su contenido en vitamina E es muy elevado, por lo cual es perfecto para el beneficio de la piel, las uñas y el pelo y su correcta nutrición. Son muy energéticas, aportan alrededor de 600 calorías cada 100 gramos. También aportan fibras y minerales como el calcio, por lo que son muy recomendables para evitar problemas óseos.
<br><br>
La relación sodio/potasio que tiene la avellana es notablemente favorable al potasio, por lo que es ideal para controlar la hipertensión arterial. Las grasas de la avellana son insaturadas, con predominio del ácido oleico. Todo ello ayuda a mantener el colesterol controlado. Las avellanas al tener una cantidad elevada de fibra son idóneas para combatir el estreñimiento. También son beneficiosas para prevenir la formación de cálculos renales.
