                        <h2 style="color: white;">Sus propiedades</h2>
                        <p>
Es un alimento muy nutritivo, con un alto valor calórico, 662,5-688 kcal/100g y entre las vitaminas presentes en las nueces, las más importantes son las del grupo B, entre las que destaca el ácido fólico. Sus componentes son:                       
<br>
                        <ul>                            

<li>Agua 4%

<li>Hidrato de carbono 15% (fibra 5%)

<li>Proteínas 15%

<li>Lípidos 60% (ácido linoleico 30%)

<li>Sodio 4 mg/100 g

<li>Potasio 500 mg/100 g

<li>Calcio 80 mg/100 g

<li>Fósforo 400 mg/100 g

<li>Hierro 2 mg/100 g

<li>Vitamina B1 0,3 mg/100 g

<li>Vitamina B2 0,1 mg/100 g


                        </ul>
<br><br>
Esta composición hace que la virtud más conocida de la nuez es que es buena para el corazón porque reduce los niveles de colesterol en sangre. Sin embargo, pocos saben que logra este efecto gracias a que concentra un alto porcentaje de grasa omega 3, semejante a la que poseen los pescados azules como la merluza, el pez espada, la sardina o el atún.
<br><br>
Por su alto contenido calórico, las nueces deben formar parte de una dieta saludable, incorporándolas con cuidado. Lo ideal, indican los expertos, es que consumas de 3 a 4 unidades por día ya sea en forma individual o en los desayunos o comidas.
<br><br>
Al ser un producto oleaginoso, más de las 3/5 partes del peso de la nuez es grasa. Por eso, unos 30 gramos —entre 4 y siete nueces dependiendo de su tamaño— aportan al día unas 180 calorías, que no es poco.
</p>
