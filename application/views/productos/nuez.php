                        <h2 style="color: white;">Su origen</h2>
                        <p>
Procedente de Persia (región del Himalaya), según unos autores, o de China y Japón, según otros; fue transportado a Grecia y luego a Italia y a los demás países de Europa. Existen evidencias fósiles de la presencia del nogal J. regia, en la Península Ibérica, que se remontan al Paleolítico. El nogal se encuentra vegetando en estado silvestre en la Europa Oriental y Asia Menor, asimismo en Norteamérica. Necesita de climas templados suaves, porque no resiste las heladas. En Europa crece tanto a nivel del mar, como en las montañas de climas templados más húmedos, pero nunca con heladas o temperaturas muy bajas. Actualmente, lo cultivan en zonas templadas de Europa, Asia, África y América.
<br><br>
En el caso de México, la nuez pecanera es la que destaca tanto por su origen como por la apropiación de la producción principalmente en la zona norte del país. El árbol de pecan es el único nogal de origen americano, nativo del norte de México y sur de Estados Unidos, su conocida nuez pecanera es la semilla de su fruto, que se encuentra dentro de una cáscara indehiscente o endocarpio leñoso, formada de dos lóbulos carnosos comestibles. A diferencia de otras nueces, la nuez pecanera (Carya illinoensis) puede distinguirse por su cáscara delgada, denominada también “cáscara de papel”, misma que facilita el proceso de “quebrado”, esta nuez se caracteriza por un sabor y aroma agradable. México es el segundo productor mundial de Nuez Pecanera después de Estados Unidos, se cosecha entre octubre y diciembre con las variedades Western, Wichita y criollas, principalmente.
<br><br>    
                        </p>
