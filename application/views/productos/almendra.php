                        <h2 style="color: white;">Su origen</h2>
                        <p>
El almendro tiene su origen en las regiones montañosas de Asia Central. La proximidad de las poblaciones silvestres naturales con centros de civilización en las montañas de Asia Central hicieron posible su cultivo desde épocas remotas. La difusión a diferentes países asiáticos se vio favorecida por el hecho de que la semilla era al mismo tiempo la unidad de propagación y la parte comestible. De este modo se distribuyó por Persia, Mesopotamia y, a través de rutas comerciales, por todas las civilizaciones primitivas.
<br><br>
El almendro se cultiva en España desde hace más de 2.000 años, probablemente introducido por los fenicios y posteriormente propagado por los romanos, ya que ambos lo hicieron motivo de comercio, como se ha comprobado por los restos hallados en naves hundidas. Su cultivo se estableció al principio en las zonas costeras, donde sigue predominando, pero también se ha introducido hacia el interior e incluso en las zonas del norte, donde el clima no le es muy favorable. En el contexto actual España se encuentra dentro de los mayores productores de almendras, junto a Estados Unidos y países como Irán, Marruecos, Túnez, Turquía, Argelia conforman la lista de los mayores productos de la semilla. 
<br><br>
                        </p>
