                        <br><br>
Los manís o cacahuetes son alimentos naturales con propiedades alimentarias ampliamente reconocidas. Destacan por su riqueza en vitaminas del grupo B, por su elevado contenido en grasas y por su enorme cantidad de proteínas.
<br><br>
Además de sus propiedades alimentarias, medicinalmente los cacahuetes contienen componentes que les otorgan valores especiales para la salud. Las principales propiedades medicinales se encuentran en el fruto de los cacahuetes y en su aceite; el aceite obtenido de las semillas ejerce una acción antiesclerótica, hipocolesterolémica, y una débil acción estrogénica y antitiroidal.
