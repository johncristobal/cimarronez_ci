                        <h4 style="color: white;">Propiedades antioxidantes de los cacahuates</h4>
                        <p>
Los cacahuates, al igual que las uvas negras, son ricos en resveratrol, un componente con propiedades antioxidantes muy destacadas. Las propiedades del resveratrol en el control del colesterol y la prevención de las enfermedades circulatorias ha sido ampliamente destacadas en numerosos estudios.
<br><br>
Se ha comprobado que en la piel de los cacahuates se encuentra otro componente con propiedades antioxidantes denominado procianidina oligomérica. Al igual que el resveratrol, se trata de otro flavonoide con propiedades anti-arteriosclerosis.
<br><br>
Gracias a este componente, junto con el resveratrol, el cacahuate es capaz de prevenir las enfermedades del corazón y de las arterias o el cáncer.
<br><br>
<h4 style="color: white;">Cacahuates para la salud de la mujer</h4>
<p>
Otro componente con muchas propiedades beneficiosas para la salud de la mujer es el fitoestrógeno genisteína. Los cacahuates poseen una cantidad muy elevada de genisteína , incluso superior a la soja. Se ha comentado mucho el poder de la genisteína de la soja para combatir los efectos negativos del síndrome premenstrual, como los sofocos o la depresión. La genisteína también posee propiedades antioxidantes muy reconocidas, especialmente en lo que se refiere a la prevención del desarrollo de células cancerosas y en la prevención de la perdida de la visión por degeneración macular en la vista de las personas con diabetes.
<br><br>
Se cree que los cacahuates poseen propiedades galactogogas, es decir que son capaces de estimular la producción de la leche en las lactantes . Por este motivo, en China, es común que las mujeres que amamantan a sus bebes con leche materna coman cacahuates. Esto podría contradecir el pensamiento de muchos especialistas occidentales que piensan que no resulta adecuado comer este alimento en mujeres embarazadas por miedo a la alergia alimentaria o a la sensibilización del bebe a futuras alergias con respecto a este alimento.
<br><br>
Gracias a la procianidina oligomérica, también se le concede a este componente la propiedad de ser muy útil en el control de la endometriosis.
<br><br>

</p>
<h4 style="color: white;">Cacahuates para conservar las defensas</h4>
<p>
Los cacahuates son muy ricos en cinc. El cinc, entre otras propiedades, tiene la facultad de proteger las defensas e impedir que el sistema linfático cometa errores defensivos. Comer cacahuates puede ayudar a mantener el sistema inmune en buen estado, previniendo la aparición de infecciones.
<br><br>
</p>
<h4 style="color: white;">Cacahuates para evitar las depresión, falta de atención e hiperactividad</h4>
<p>
Los cacahuates son muy ricos en tirosina un aminoácido que forma parte de las proteínas del cacahuate. A partir de la tirosina el organismo produce dopamina. La dopamina, la serotonina y la noradrenalina, constituyen las llamadas ” hormonas de la felicidad”.
<br><br>

</p>
