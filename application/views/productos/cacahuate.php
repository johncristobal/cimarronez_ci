                        <h2 style="color: white;">Su origen</h2>
                        <p>
                        La planta del cacahuete es originaria del sur de Brasil; región tropical; género nativo del sur de América, la especie se desarrolla ampliamente en regiones tropicales y templadas. Habita en climas cálido y semicálido entre los 160 y los 1200 msnm. Planta cultivada en huertos familiares, comestible, asociada a bosque tropical subcaducifolio.   
                        <br><br>
                        La planta del cacahuate es una hierba anual de entre 30 y 50 cm de altura. Tallos tendidos o erectos. Hojas compuestas, pinnadas, largamente pecioladas con cuatro folíolos. Flores generalmente amarillas con venas rojizas y cáliz tubular, reunidas en grupos de entre dos y seis flores cada uno.
                        <br><br>
                        Cuando las flores inferiores maduran, los largos tallos florares se doblan hacia el suelo penetrando en el mismo. Una vez debajo del suelo, los frutos (cacahuates) maduran debajo tierra. Se trata de una peculiaridad botánica única en el mundo vegetal. De hecho el nombre de la especie “hypogea” significa en griego “debajo de la tierra”.
                        <br><br>
                        Como todas las especies de la familia de las leguminosas, presenta nódulos que tienen la capacidad de fijar el nitrógeno atmosférico. Los frutos son legumbres cuyas vainas, a medida que maduran, se vuelven duras y secas. En su interior se encuentran las semillas (cacahuates) envueltas por una capa papirácea de color rojizo.
                        </p>
