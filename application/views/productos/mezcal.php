                    En México, los diversos conocimientos y técnicas de aprovechamiento de los agaves representan, cada uno de ellos, parte esencial del patrimonio cultural de los pueblos. La identificación del valor patrimonial de los magueyes y mezcales de las diferentes regiones de México se sustentan en la historia, en la tradición misma de su aprovechamiento.                         <br>
                    <br><br>
                    La elaboración de fibras vegetales; la sabiduría de las aplicaciones medicinales del maguey, la creatividad en las recetas culinarias; los conocimientos y técnicas tradicionales para la producción de mezcales, además de la preservación y rescate de las poblaciones de maguey, entre otros, constituyen importantes manifestaciones del patrimonio cultural intangible del pueblo mexicano. Las diferentes maneras en que las comunidades rurales se refieren al maguey, los mitos y deidades relacionadas con los agaves y sus néctares, además de las actividades cotidianas relacionadas con los usos del maguey forman parte del universo simbólico que trasciende lo material para revelar la memoria, la herencia de las tradiciones y actividades artesanales de los pueblos de México.                    
