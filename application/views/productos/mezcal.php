                        <p>
                        La palabra mezcal se deriva de los vocablos náhuatl metl e ixcalli, que significan “agave cocido al horno”. El mezcal es una bebida que se obtiene por la destilación y rectificación de los mostos preparados directamente con los azúcares extraídos de las cabezas maduras de los agaves, mismas que son previamente cocidas y sometidas a fermentación alcohólica.
                        <br>
                        <br>
                        Las plantas utilizadas para elaborar mezcales se llaman agaves o magueyes y pertenecen a la familia botánica de las Agavaceas. Esta familia de plantas es endémica del continente Americano, incluye nueve géneros y casi 330 especies diferentes. El género Agave es el más representativo y diverso con 200 especies, de las cuales 150 se distribuyen en México, y 104 son endémicas al territorio nacional. Los agaves se distribuyen en todo el continente, de manera silvestre o cultivada. Se presentan y desarrollan mejor en ambientes áridos y semisecos aunque también, como ornamento, se adaptan a ambientes templados y húmedos. El nombre Agave proviene del griego y significa admirable. Este nombre fue elegido por Carl von Linneo, un naturalista sueco, para clasificar a la familia botánica de los magueyes en el año 1753.
                        </p>
