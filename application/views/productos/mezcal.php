                        <p>
                        El mezcal es un producto netamente indígena y campesino, asociado a fiestas, ritos y curaciones. El mexcalli es uno de los alimentos más importantes de los pueblos preagrícolas de Meso y Aridoamérica. En la cueva de Tehuacán se encontraron restos de maguey cocido y masticado fechados en 10,000 años de antigüedad; elaborado en hornos de piso, cociendo las cabezas y/o los quiotes durante varios días. Las evidencias arqueológicas encontraron hornos de piso desde la Península de Yucatán hasta Arizona.
                        <br><br>
                        El mexcalli es entonces alimento básico de los pueblos prehispánicos, tan importante o más que el maíz, en algunas regiones.
                        <br><br>
                        El mezcal es una bebida destilada auténticamente mexicana, ya que los agaves se distribuyen por todo el territorio. Para el proceso de elaboración de mezcales se utilizan 30 especies diferentes de agaves; por cada región y maguey empleados los mezcales tradicionales de México adoptan sus distintos nombres: Bacanora, Sotol, Minero, Arroqueño, Quitupeño, Zihuaquio, Comiteco, Tonaya, Tequila, Papalote, Tobalà, Raicilla, entre muchos otros.
