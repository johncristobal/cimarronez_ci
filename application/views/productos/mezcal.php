                    <p>
                        El proceso de producción de mezcal tradicional se compone de cinco etapas:
                        <br>
                        <ul>
                            <li>Selección y corte del maguey maduro</li>
                            <li>Horneado o cocimiento de las piñas de maguey</li>
                            <li>Machacado o molienda de las piñas cocidas</li>
                            <li>Fermentación</li>
                            <li>Destilación</li>
                        </ul>
                    <br><br>
                    Las técnicas y los materiales son similares en todas las fábricas de mezcal, aunque existen sutiles diferencias que caracterizan a cada una según la tradición. Por ejemplo, la variedad de maguey empleado, la molienda, las herramientas, el tipo de horno y alambique.
                    <br><br>
                    Distintos maestros mezcaleros coinciden en sus propiedades terapéuticas. “El mezcal –dice uno de ellos, Don Moisés- lo han utilizado anteriormente como medicina. Cura la presión. Que si una señora acaba de recibir un susto y acaba de tener a su bebito, lo primero es darle su copa de mezcal. Lo toman como remedio. Ayuda a los nervios, tranquiliza demasiado. Para algún coraje que haga, lo primero es darle su copa de mezcal. Siempre ha sido curativo. Está garantizado. Es saludable de por sí.”
                    <br><br>
                    A pesar de ello, durante mucho tiempo el mezcal fue una bebida de “segunda” y supuestamente inferior a la cerveza o a los destilados de uva y caña de azúcar fabricados por grandes casas productoras y no sólo fue de “segunda” también fue prohibida su producción, en algunas regiones del estado de Guerrero hasta finales de la década de los 90´s del siglo anterior. Quizá esa percepción y esa prohibición se relacione con su origen indígena y campesino y el colonialismo que pesa sobre ellos, sin embargo en las comunidades productoras nunca a dejado de ser motivo de honor y orgullo, motivo de construcción de historias de resistencia cultural, de experiencia productiva artesanal de años, de conservación de los suelos, de solidaridad y cooperación.
                    
